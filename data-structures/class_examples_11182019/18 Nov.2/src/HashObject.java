
public class HashObject {
	private int data;
	private boolean prevUsed;

	public int getData() {
		return data;
	}
	public void setData(int data) {
		this.data = data;
	}
	public boolean isPrevUsed() {
		return prevUsed;
	}
	public void setPrevUsed(boolean prevUsed) {
		this.prevUsed = prevUsed;
	}

}
